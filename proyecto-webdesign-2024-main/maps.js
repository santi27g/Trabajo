function loadImage(sourceUrl) {
  const img = document.createElement('img');

  img.src = ASSETS_DIR + sourceUrl;

  img.addEventListener('error', () => {
    console.log('Not found image with source: ' + sourceUrl);
    img.src = ASSETS_DIR + 'placeholder.png';
  });
  return img;
}

